package com.example.demo.logic.Converters;

public class UnvalidConverterException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	

	public UnvalidConverterException() {
		super();
	}

	public UnvalidConverterException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnvalidConverterException(String message) {
		super(message);
	}

	public UnvalidConverterException(Throwable cause) {
		super(cause);
	}
	
	

}
